package web_project.home;

public class LedBean {
	private int r_aid;
	private int r_led;
	private int r_state;
	private String r_name;
	
	public int getR_aid() {
		return r_aid;
	}
	public void setR_aid(int r_aid) {
		this.r_aid = r_aid;
	}
	public int getR_state() {
		return r_state;
	}
	public void setR_state(int r_state) {
		this.r_state = r_state;
	}
	public int getR_led() {
		return r_led;
	}
	public void setR_led(int r_led) {
		this.r_led = r_led;
	}
	public String getR_name() {
		return r_name;
	}
	public void setR_name(String r_name) {
		this.r_name = r_name;
	}
	
}
