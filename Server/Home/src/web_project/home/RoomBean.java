package web_project.home;

public class RoomBean {
	private int r_id;
	private String r_name;
	private double r_cal;
	private double r_row;
	private double r_height;
	private int r_sensor;
	
	public int getR_id() {
		return r_id;
	}
	public void setR_id(int r_id) {
		this.r_id = r_id;
	}
	public String getR_name() {
		return r_name;
	}
	public void setR_name(String r_name) {
		this.r_name = r_name;
	}
	public double getR_cal() {
		return r_cal;
	}
	public void setR_cal(double r_cal) {
		this.r_cal = r_cal;
	}
	public double getR_row() {
		return r_row;
	}
	public void setR_row(double r_row) {
		this.r_row = r_row;
	}
	public double getR_height() {
		return r_height;
	}
	public void setR_height(double r_height) {
		this.r_height = r_height;
	}
	public int getR_sensor() {
		return r_sensor;
	}
	public void setR_sensor(int r_sensor) {
		this.r_sensor = r_sensor;
	}
	
}
